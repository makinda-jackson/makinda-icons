// Makinda Icons — JetBrains plugin emitter
//
// Reads scripts/manifest.mjs (the single source of truth) and emits everything
// the IntelliJ Platform plugin needs:
//
//   • packages/jetbrains/src/main/resources/icons/makinda/<name>.svg       (light)
//   • packages/jetbrains/src/main/resources/icons/makinda/<name>_dark.svg  (dark, auto-picked by JB)
//   • packages/jetbrains/src/main/kotlin/com/makinda/icons/MakindaFileIconProvider.kt
//   • packages/jetbrains/src/main/resources/META-INF/plugin.xml
//
// Run: `npm run build:jetbrains`
//
// Uses the same upstream source root + recoloring rules as the VS Code build.
// File icons get two color variants because IntelliJ's New UI also ships
// monochrome icons that look better when recolored to the active foreground.

import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import { fileIcons } from "./manifest.mjs";
import { enabledStyles } from "./styles.mjs";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const ICONS_SRC_ROOT = process.env.ICONS_SRC_ROOT
    || "/Users/makindajack/Downloads/Compressed/icons-src/25,000+ SVG icons";

// IntelliJ's recommended foreground tints for monochrome icons. These match
// Makinda's dark/light fg.default and the VS Code variants for visual parity.
const DARK_FILL = "#e7e8ea";
const LIGHT_FILL = "#1e2022";

// Use the first enabled style (usually `solid`). The JetBrains plugin ships
// one style per release; pick a different one with `STYLE=duotone npm run …`.
const styleId = process.env.STYLE || "solid";
const style = enabledStyles().find((s) => s.id === styleId);
if (!style) {
    console.error(`Unknown or disabled style "${styleId}". Enable it in scripts/styles.mjs first.`);
    process.exit(1);
}

const jbRoot = path.join(repoRoot, "packages", "jetbrains");
const iconsOutDir = path.join(jbRoot, "src", "main", "resources", "icons", "makinda");
const kotlinOutDir = path.join(jbRoot, "src", "main", "kotlin", "com", "makinda", "icons");
const metaInfDir = path.join(jbRoot, "src", "main", "resources", "META-INF");

for (const dir of [iconsOutDir, kotlinOutDir, metaInfDir]) {
    fs.mkdirSync(dir, { recursive: true });
}

// Wipe the icons dir so removed manifest entries don't linger.
for (const f of fs.readdirSync(iconsOutDir)) {
    if (f.endsWith(".svg")) fs.unlinkSync(path.join(iconsOutDir, f));
}

function resolveSrc(rel) {
    if (path.isAbsolute(rel)) return rel;
    if (style.srcRoot && !rel.startsWith("../")) {
        return path.join(ICONS_SRC_ROOT, style.srcRoot, rel);
    }
    return path.join(ICONS_SRC_ROOT, rel);
}

function recolor(svg, color) {
    return svg
        .replace(/fill\s*=\s*"#[0-9a-fA-F]{3,8}"/g, `fill="${color}"`)
        .replace(/stroke\s*=\s*"#[0-9a-fA-F]{3,8}"/g, `stroke="${color}"`);
}

let copied = 0;
const missing = [];
const emitted = []; // { name, extensions, fileNames, languageIds }

for (const icon of fileIcons) {
    const srcAbs = resolveSrc(icon.src);
    if (!fs.existsSync(srcAbs)) {
        missing.push(`${icon.name}: ${icon.src}`);
        continue;
    }
    const svg = fs.readFileSync(srcAbs, "utf8");
    fs.writeFileSync(path.join(iconsOutDir, `${icon.name}.svg`), recolor(svg, LIGHT_FILL));
    fs.writeFileSync(path.join(iconsOutDir, `${icon.name}_dark.svg`), recolor(svg, DARK_FILL));
    copied++;
    emitted.push({
        name: icon.name,
        extensions: icon.extensions ?? [],
        fileNames: icon.fileNames ?? [],
    });
}

// ---- Generate Kotlin provider ----

const byExt = {};
const byName = {};
for (const e of emitted) {
    for (const ext of e.extensions) byExt[ext.toLowerCase()] = e.name;
    for (const fn of e.fileNames) byName[fn.toLowerCase()] = e.name;
}

const iconKeys = [...new Set(emitted.map((e) => e.name))].sort();

function kotlinMapEntries(map) {
    return Object.entries(map)
        .sort(([a], [b]) => a.localeCompare(b))
        .map(([k, v]) => `        "${k.replace(/\\/g, "\\\\").replace(/"/g, "\\\"")}" to ICON_${v.replace(/-/g, "_").toUpperCase()},`)
        .join("\n");
}

const kotlinSrc = `// AUTO-GENERATED by scripts/build-jetbrains.mjs — DO NOT EDIT BY HAND.
// Source of truth: scripts/manifest.mjs
package com.makinda.icons

import com.intellij.ide.FileIconProvider
import com.intellij.openapi.project.Project
import com.intellij.openapi.util.IconLoader
import com.intellij.openapi.vfs.VirtualFile
import javax.swing.Icon

class MakindaFileIconProvider : FileIconProvider {
    override fun getIcon(file: VirtualFile, flags: Int, project: Project?): Icon? {
        if (file.isDirectory) return null
        val name = file.name.lowercase()
        BY_NAME[name]?.let { return it }
        val ext = file.extension?.lowercase() ?: return null
        return BY_EXT[ext]
    }

    companion object {
        private fun load(name: String): Icon =
            IconLoader.getIcon("/icons/makinda/$name.svg", MakindaFileIconProvider::class.java)

${iconKeys.map((k) => `        private val ICON_${k.replace(/-/g, "_").toUpperCase()}: Icon = load("${k}")`).join("\n")}

        private val BY_EXT: Map<String, Icon> = mapOf(
${kotlinMapEntries(byExt)}
        )

        private val BY_NAME: Map<String, Icon> = mapOf(
${kotlinMapEntries(byName)}
        )
    }
}
`;

fs.writeFileSync(path.join(kotlinOutDir, "MakindaFileIconProvider.kt"), kotlinSrc);

// ---- Generate plugin.xml ----

const pkgJson = JSON.parse(fs.readFileSync(path.join(repoRoot, "package.json"), "utf8"));
const pluginXml = `<idea-plugin>
    <id>com.makinda.icons</id>
    <name>Makinda Icons</name>
    <vendor email="hello@makinda.dev" url="https://github.com/makindajack/makinda-icons">Makinda Jackson</vendor>

    <description><![CDATA[
        File icons for the IntelliJ Platform — tuned for the Makinda brand, tuned for the Makinda brand.<br/>
        Same icon set as the <a href="https://marketplace.visualstudio.com/items?itemName=makindajack.makinda-icons">Makinda Icons VS Code extension</a>.<br/><br/>
        Currently shipping the <b>Solid</b> style (${copied} file icons). Auto-adapts to light/dark themes.
    ]]></description>

    <change-notes><![CDATA[
        <h3>1.0.0</h3>
        <ul>
            <li>Initial JetBrains release: ${copied} file icons via FileIconProvider</li>
            <li>Auto light/dark via <code>_dark.svg</code> variants</li>
        </ul>
    ]]></change-notes>

    <depends>com.intellij.modules.platform</depends>

    <extensions defaultExtensionNs="com.intellij">
        <fileIconProvider implementation="com.makinda.icons.MakindaFileIconProvider"/>
    </extensions>
</idea-plugin>
`;

fs.writeFileSync(path.join(metaInfDir, "plugin.xml"), pluginXml);

// ---- Report ----

console.log(`makinda-icons (jetbrains) build`);
console.log(`  style:           ${style.id}`);
console.log(`  ICONS_SRC_ROOT:  ${ICONS_SRC_ROOT}`);
console.log(`  icons emitted:   ${copied} (×2 with _dark variants = ${copied * 2})`);
console.log(`  ext mappings:    ${Object.keys(byExt).length}`);
console.log(`  name mappings:   ${Object.keys(byName).length}`);
console.log(`  missing:         ${missing.length}`);
for (const m of missing) console.log(`    • ${m}`);

if (missing.length) process.exit(1);
console.log("\nNext: cd packages/jetbrains && ./gradlew buildPlugin");
